public class Orang {
	private String nim, nama, prodi;
	
	public void setNo (String nim){
		this.nim = nim;
	}
	
	public String getNo (){
		return nim;
	}
	
	public void setNama (String nama){
		this.nama = nama;
	}
	
	public String getNama (){
		return nama;
	}
	
	public void setPeringkat (String prodi){
		this.prodi = prodi;
	}
	
	public String getPeringkat (){
		return prodi;
	}
}